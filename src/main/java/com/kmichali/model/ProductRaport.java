package com.kmichali.model;

public class ProductRaport {

    double storeAmount;
    String name;
    String date;
    double wholeAmount;
    String type;
    public double getStoreAmount() {
        return storeAmount;
    }

    public void setStoreAmount(double storeAmount) {
        this.storeAmount = storeAmount;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public double getWholeAmount() {
        return wholeAmount;
    }

    public void setWholeAmount(double wholeAmount) {
        this.wholeAmount = wholeAmount;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
